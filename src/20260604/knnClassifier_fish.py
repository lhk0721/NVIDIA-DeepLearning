#   한 줄 정의
#   ┌──────────────────────────┬─────────────┬─────────────────────────┬────────────────────┐
#   │           용어           │    변수     │          의미           │    이 파일에서     │
#   ├──────────────────────────┼─────────────┼─────────────────────────┼────────────────────┤
#   │ 특성 데이터 (feature, x) │ fish_data   │ 입력. "문제지"의 단서들 │ 생선의 길이 + 무게 │
#   ├──────────────────────────┼─────────────┼─────────────────────────┼────────────────────┤
#   │ 타깃 데이터 (target, y)  │ fish_target │ 정답. 맞혀야 할 답      │ 도미(1) / 빙어(0)  │
#   └──────────────────────────┴─────────────┴─────────────────────────┴────────────────────┘

#   ▎ fit(x, y) = fit(특성, 타깃) = fit(문제, 정답) → 이 둘을 짝지어 외워두는(저장) 게 KNN의 학습입니다.

#   ---
#   1. 특성 데이터 (x) — fish_data

#   생선을 무엇으로 판단할지, 그 단서들입니다. 여기선 길이와 무게 2가지 특성을 씁니다.

#   length = bream_length + smelt_length   # 길이 (특성 1)
#   weight = bream_weight + smelt_weight   # 무게 (특성 2)

#   fish_data = np.array([[l, w] for l, w in zip(length, weight)])

#   결과 모양 (49개 생선 × 2개 특성):
#   fish_data =
#   [[ 25.4  242. ]   ← 0번 생선: 길이25.4, 무게242
#    [ 26.3  290. ]   ← 1번 생선: 길이26.3, 무게290
#    [ 26.5  340. ]
#    ...
#    [ 15.0   19.9]]  ← 48번 생선 (빙어)

#   - 한 행([25.4, 242.]) = 생선 한 마리
#   - 한 열 = 하나의 특성 (1열=길이, 2열=무게)
#   - 그래서 predict도 2차원 배열을 요구합니다 → [[30, 420]] (한 마리여도 [[ ]])

#   2. 타깃 데이터 (y) — fish_target

#   각 생선이 실제로 무엇인지에 대한 정답표입니다.

#   # 1: 도미(Bream), 0: 빙어(Smelt)
#   fish_target = [1]*35 + [0]*14
#   # → [1,1,1, ... ,1, 0,0, ... ,0]
#   #    앞 35개=도미      뒤 14개=빙어

#   - 도미 데이터가 35개(bream_length가 35개), 빙어가 14개라서 35+14 = 49.
#   - 순서가 중요: fish_data의 0번 생선이 도미면 fish_target의 0번도 반드시 1이어야 합니다. (특성과 타깃이 같은 줄에서 짝을 이룸)

#   3. 둘이 만나는 곳 — fit

#   knnmodel.fit(fish_data, fish_target)
#   #            └ 특성(x) ┘  └ 타깃(y) ┘

