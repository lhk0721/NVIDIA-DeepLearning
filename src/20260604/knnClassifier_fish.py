## knn k nearest neighbor
# knn 분류와 회귀에 모두 쓰인다.
# n reverse param
# 분류 알고리즘.

#   ┌───────────────┬───────────────────────────────────────────────┐
#   │               │                 KNN이 하는 일                 │
#   ├───────────────┼───────────────────────────────────────────────┤
#   │ k             │ 학습 산출물 ❌ → 사람이 정하는 하이퍼파라미터 │
#   ├───────────────┼───────────────────────────────────────────────┤
#   │ fit(학습)     │ 훈련 데이터(특성+타깃)를 저장만 함            │
#   ├───────────────┼───────────────────────────────────────────────┤
#   │ predict(예측) │ 거리 계산 → k개 이웃 → 다수결 (실제 계산)     │
#   ├───────────────┼───────────────────────────────────────────────┤
#   │ 학습 vs 예측  │ 동일하지 않음. 오히려 예측 때 다 함           │
#   └───────────────┴───────────────────────────────────────────────┘

# 게으른 학습. 거리계산. 산재된 데이터(특성데이터) 준비 후 메모리에 올려버리고,
# 데이터 저장이 학습이다. 고샤양의 메모리가 필요하다.
# 연산자원이 많이 필요하다. 속도가 느리다.
# 최적의 k(사람이 주는 하이퍼파라미터)를 선택하기 어렵다. 과적합과 과소적합의 싸움, 데이터에 의존, 여러 선택과의 상호작용, 클래스 불균형, 평가 자체의 변동성 등

